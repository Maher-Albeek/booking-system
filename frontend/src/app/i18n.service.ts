const ARABIC_CHARACTERS = /[\u0600-\u06FF]/;
const ARABIC_MOJIBAKE = /[ØÙ]/;
const MULTI_QUESTION_MARKS = /\?{3,}/;

function decodeLatin1Utf8(value: string): string {
  if (typeof TextDecoder === 'undefined') {
    return value;
  }

  const bytes = new Uint8Array([...value].map((character) => character.charCodeAt(0) & 0xff));
  return new TextDecoder('utf-8').decode(bytes);
}

function normalizeArabicTranslation(value: string, fallback: string): string {
  let nextValue = value;

  // Recover UTF-8 Arabic that was interpreted as latin-1 (e.g. "Ø§Ù„...").
  if (ARABIC_MOJIBAKE.test(nextValue) && !ARABIC_CHARACTERS.test(nextValue)) {
    const decoded = decodeLatin1Utf8(nextValue);
    if (ARABIC_CHARACTERS.test(decoded)) {
      nextValue = decoded;
    }
  }

  // If the text still contains unrecoverable placeholder-like question marks, use English fallback.
  if (MULTI_QUESTION_MARKS.test(nextValue) && !ARABIC_CHARACTERS.test(nextValue)) {
    return fallback;
  }

  return nextValue;
}

function buildArabicTranslations(): Record<string, string> {
  const normalizedTranslations: Record<string, string> = {};

  for (const [key, value] of Object.entries(AR_TRANSLATIONS)) {
    const fallback = EN_TRANSLATIONS[key] ?? value;
    const normalizedValue = normalizeArabicTranslation(value, fallback);
    normalizedTranslations[key] = AR_TRANSLATION_OVERRIDES[key] ?? normalizedValue;
  }

  return normalizedTranslations;
}

function buildGermanTranslations(): Record<string, string> {
  const normalizedTranslations: Record<string, string> = {};

  for (const [key, value] of Object.entries(DE_TRANSLATIONS)) {
    normalizedTranslations[key] = DE_TRANSLATION_OVERRIDES[key] ?? value;
  }

  return normalizedTranslations;
}

const TRANSLATIONS: Record<LanguageCode, Record<string, string>> = {
  en: EN_TRANSLATIONS,
  de: buildGermanTranslations(),
  ar: buildArabicTranslations()
};

const LOCALE_MAP: Record<LanguageCode, string> = {
  en: 'en-GB',
  de: 'de-DE',
  ar: 'ar-EG'
};

@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly document = inject(DOCUMENT);
  private readonly activeLanguage = signal<LanguageCode>(this.readInitialLanguage());

  readonly language = this.activeLanguage.asReadonly();
  readonly locale = computed(() => LOCALE_MAP[this.activeLanguage()]);
  readonly direction = computed<'ltr' | 'rtl'>(() =>
    this.activeLanguage() === 'ar' ? 'rtl' : 'ltr'
  );

  constructor() {
    effect(() => {
      const language = this.activeLanguage();
      const direction = this.direction();

      this.document.documentElement.lang = language;
      this.document.documentElement.dir = direction;
      this.persistLanguage(language);
    });
  }

  setLanguage(language: LanguageCode): void {
    this.activeLanguage.set(language);
  }

  t(key: string, params?: Record<string, string | number>): string {
    const language = this.activeLanguage();
    const template = TRANSLATIONS[language][key] ?? EN_TRANSLATIONS[key] ?? this.humanizeKey(key);

    if (!params) {
      return template;
    }

    return template.replace(/\{([^}]+)\}/g, (match, rawName: string) => {
      const name = rawName.trim();
      return Object.prototype.hasOwnProperty.call(params, name)
        ? String(params[name])
        : match;
    });
  }

  private humanizeKey(key: string): string {
    const segment = key.split('.').pop() ?? key;
    const spaced = segment
      .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
      .replace(/_/g, ' ')
      .trim();

    if (!spaced) {
      return key;
    }

    return spaced.charAt(0).toUpperCase() + spaced.slice(1);
  }

  private readInitialLanguage(): LanguageCode {
    const persistedLanguage = this.readPersistedLanguage();
    if (persistedLanguage) {
      return persistedLanguage;
    }

    if (typeof navigator === 'undefined') {
      return 'en';
    }

    const browserLanguage = navigator.language.toLowerCase();

    if (browserLanguage.startsWith('de')) {
      return 'de';
    }

    if (browserLanguage.startsWith('ar')) {
      return 'ar';
    }

    return 'en';
  }

  private readPersistedLanguage(): LanguageCode | null {
    try {
      if (typeof localStorage === 'undefined') {
        return null;
      }

      const value = localStorage.getItem(STORAGE_KEY);
      return value === 'en' || value === 'de' || value === 'ar' ? value : null;
    } catch {
      return null;
    }
  }

  private persistLanguage(language: LanguageCode): void {
    try {
      if (typeof localStorage === 'undefined') {
        return;
      }

      localStorage.setItem(STORAGE_KEY, language);
    } catch {
      // Ignore storage failures and keep language in-memory.
    }
  }
}
